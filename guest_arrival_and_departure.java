    public void idload() {
        try {
            ResultSet rs = DB.search("select id from guest_arr_dep order by id desc");
            if (rs.next()) {
                String id = rs.getString("id");
                int id1 = Integer.parseInt(id) + 1;
                jLabel15.setText(String.valueOf(id1));

            } else {
                jLabel15.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void tableload() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("select * from guest_arr_dep where id = '" + jLabel15.getText() + "'");
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("t_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("pass_num"));
                v.add(rs.getString("a_date"));
                v.add(rs.getString("a_flight_num"));
                v.add(rs.getString("d_date"));
                v.add(rs.getString("d_flight_num"));
                v.add(rs.getString("remark"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

