    private void jTextField11KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField11KeyReleased
        // TODO add your handling code here:
        try {

            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jDateChooser1.setDate(null);
            jDateChooser2.setDate(null);

            if (jRadioButton1.isSelected()) {
                ResultSet rs = DB.search("select * from t_create where t_id like '%" + jTextField11.getText() + "%'");
                Vector v1 = new Vector();
                while (rs.next()) {
                    jTextField1.setText(rs.getString("t_id"));
                    jTextField2.setText(rs.getString("t_name"));
                    jTextField3.setText(rs.getString("pass_num"));

                    if (!rs.getString("t_id").equals(jTextField11.getText())) {
                        v1.add(rs.getString("t_id") + ":" + rs.getString("t_name") + "-" + rs.getString("pass_num"));
                    }
                }
                jList1.setListData(v1);

            }

            if (jRadioButton2.isSelected()) {
                jTextField1.setEnabled(false);
                ResultSet rs = DB.search("select * from client_r where pass_num like '%" + jTextField11.getText() + "%'");
                Vector v1 = new Vector();
                while (rs.next()) {
                    // jTextField1.setText(rs.getString("t_id"));
                    jTextField2.setText(rs.getString("c_name"));
                    jTextField3.setText(rs.getString("pass_num"));

                    if (!rs.getString("pass_num").equals(jTextField11.getText())) {
                        v1.add(rs.getString("pass_num") + ":" + rs.getString("c_name"));
                    }
                }
                jList1.setListData(v1);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField11KeyReleased

